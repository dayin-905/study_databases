--
-- 데이터베이스 기초 SQL 문제 풀이 최종 답안
--
-- =========================================================
-- 📌 문제 1 — 테이블 생성 (PRIMARY KEY 기초)
-- 요구사항: students 테이블 생성, id를 PRIMARY KEY로 지정
-- =========================================================

-- DROP TABLE students; -- 테스트 환경에서 테이블이 이미 존재할 경우를 대비해 사용 (선택 사항)

CREATE TABLE students (
    id INT PRIMARY KEY,         -- 고유 식별자, 기본 키 (PRIMARY KEY)
    name VARCHAR(50),           -- 학생 이름 (최대 50자)
    age INT                     -- 학생 나이
);

-- =========================================================
-- 📌 문제 2 — CREATE (INSERT) 기초
-- 요구사항: students 테이블에 세 명의 학생 데이터 삽입
-- =========================================================

INSERT INTO students (id, name, age) VALUES
(1, '홍길동', 23),
(2, '이영희', 21),
(3, '박철수', 26);

-- =========================================================
-- 📌 문제 3 — READ (SELECT) 기본 조회
-- 요구사항: 조건에 맞는 SELECT 쿼리 작성
-- =========================================================

-- 3-1: students 테이블의 전체 데이터 조회
SELECT *
FROM students;

-- 3-2: 나이가 22세 이상인 학생만 조회 (결과: 홍길동, 박철수)
SELECT *
FROM students
WHERE age >= 22;

-- 3-3: name 이 "홍길동"인 학생만 조회 (결과: 홍길동)
SELECT *
FROM students
WHERE name = '홍길동';

-- =========================================================
-- 📌 문제 4 — UPDATE 연습
-- 요구사항: id = 2 인 학생 (이영희)의 나이를 25로 수정
-- =========================================================

UPDATE students
SET age = 25
WHERE id = 2;

-- 수정 후 확인 (선택 사항)
-- SELECT * FROM students WHERE id = 2;

-- =========================================================
-- 📌 문제 5 — DELETE 연습
-- 요구사항: id = 3 번 학생 (박철수) 데이터를 삭제
-- =========================================================

DELETE FROM students
WHERE id = 3;

